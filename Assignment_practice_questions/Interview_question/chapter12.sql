1) What is the role of index?
An index improves the speed of data retrieval from a table.

2) Do you use index directly?
No, indexes are used automatically by the database to optimize queries.

3) Can an index be created on last name column in Employees table?
Yes, an index can be created on any column such as the last name column.

4) What are views used for?
Views are used to display data from one or more tables as a virtual table.

5) Do views store data?
No, views do not store data; they store only the query definition.

6) How is sequence helpful?
A sequence is used to generate unique numbers automatically.