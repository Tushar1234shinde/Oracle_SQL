1) What are different DML statements?
The main DML statements are INSERT, UPDATE, DELETE, and SELECT.

2) What is the difference between TRUNCATE and DELETE?
DELETE removes rows with a condition and can be rolled back, while TRUNCATE removes all rows quickly and cannot be rolled back.

3) What is a transaction?
A transaction is a sequence of SQL operations performed as a single unit of work.

4) Which type of statements are ROLLBACK and COMMIT?
ROLLBACK and COMMIT are Transaction Control Language (TCL) statements.

5) What is read consistency?
Read consistency ensures that a query retrieves a consistent snapshot of data at a specific point in time.