1) What is a Join?
A join is used to combine rows from two or more tables based on a related column.

2) What are the different types of joins?
Inner Join, Left Join, Right Join, Full Join, Self Join, and Cross Join.

3) What is an equi join or inner join?
An equi join (inner join) returns matching rows from two tables based on equality of columns.

4) What is a self join?
A self join is a join where a table is joined with itself.

5) What is a non-equi join?
A non-equi join is a join where columns are compared using operators other than equality (=).