1) What is a privilege?
A privilege is a permission given to a user to perform specific operations on a database.

2) What is a role?
A role is a group of privileges that can be assigned to users.

3) How do you revoke privilege?
Privileges are removed using the REVOKE command.

4) What is the difference between system privilege and object privilege?
System privileges allow actions on the database system, while object privileges allow actions on specific database objects like tables or views.