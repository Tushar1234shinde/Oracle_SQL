1) Which clause is used with SELECT statement to filter the data?
The WHERE clause is used to filter data in a SELECT statement.

2) Which clause is used with SELECT statement to order or sort the data?
The ORDER BY clause is used to sort the data.

3) Can you fetch records from the table based on some input from the user? How?
Yes, by using the WHERE clause with conditions or parameters.

4) What is the rule of precedence?
Rule of precedence defines the order in which operators are evaluated (NOT → AND → OR).

5) Is BETWEEN ... AND operator inclusive or exclusive operator?
The BETWEEN ... AND operator is inclusive of both boundary values.