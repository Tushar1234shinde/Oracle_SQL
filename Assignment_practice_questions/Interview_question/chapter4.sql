1) What is the difference between single row functions and group functions?
Single row functions operate on each row individually, while group functions operate on multiple rows and return a single result.

2) Give 5 examples of single row functions.
UPPER(), LOWER(), LENGTH(), ROUND(), SUBSTR().

3) What arithmetic operations can be performed with dates?
Dates can be added to or subtracted from numbers to calculate days.

4) What is RR format of date?
RR date format allows storing two-digit years and automatically determines the correct century.

5) Name any two character-based single row functions.
UPPER() and LOWER().

6) Name any two arithmetic single row functions.
ROUND() and MOD().