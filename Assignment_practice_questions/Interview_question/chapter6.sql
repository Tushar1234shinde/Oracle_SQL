1) Name any 5 group functions.
SUM(), AVG(), COUNT(), MAX(), MIN().

2) When is GROUP BY clause used? Explain with an example.
GROUP BY is used to group rows having the same values for aggregate functions.

3) How to exclude rows based on group functions from the output?
Rows are excluded using the HAVING clause.

4) Can you nest two group functions?
Yes, group functions can be nested.

5) Can you nest a single row function inside a group function?
Yes, a single row function can be used inside a group function.