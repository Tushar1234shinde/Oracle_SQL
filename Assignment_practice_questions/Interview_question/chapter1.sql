1) What is DBMS?
DBMS (Database Management System) is software used to create, manage, and control databases.

2) What is a Database?
A database is an organized collection of related data stored in structured form.

3) What is Data Integrity?
Data integrity ensures the accuracy and consistency of data.

4) What is Data Redundancy?
Data redundancy is the unnecessary duplication of data.

5) What is Data Inconsistency?
Data inconsistency occurs when the same data has different values in different places.

6) What is Physical Data Independence?
Physical data independence means changes in storage do not affect the logical structure.

7) What is Logical Data Independence?
Logical data independence means changes in table structure do not affect applications.

8) What is RDBMS?
RDBMS is a database system that stores data in tables with relationships.

9) What is Relation in RDBMS?
A relation is a table consisting of rows and columns.

10) What is SQL?
SQL (Structured Query Language) is used to manage and manipulate relational databases.