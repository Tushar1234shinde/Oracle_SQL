1) What are different conversion functions in SQL?
Common conversion functions are TO_CHAR(), TO_DATE(), and TO_NUMBER().

2) Can you convert a date into character format?
Yes, by using the TO_CHAR() function.

3) What is the significance of NVL and NVL2 functions?
NVL replaces NULL with a specified value, while NVL2 returns one value if NULL is not present and another if it is NULL.